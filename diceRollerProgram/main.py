# Get number of dice
num_of_dice = int(input("How many dice?: "))

# Roll dice
dice = [random.randint(1, 6) for _ in range(num_of_dice)]

# Print dice horizontally
for line_index in range(5):
    for die in dice:
        print(dice_art[die][line_index], end=" ")
    print()

# Calculate total
total = sum(dice)
print(f"\nTotal: {total}")
